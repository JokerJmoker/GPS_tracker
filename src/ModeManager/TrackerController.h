#ifndef TRACKER_CONTROLLER_H
#define TRACKER_CONTROLLER_H

#include <Arduino.h>
#include "NEO/NEO.h"
#include "SIM/SIM.h"
#include "MPU/MPU.h"
#include "SystemModes.h"

// ============================================
// КЛАСС КОНТРОЛЛЕР ТРЕКЕРА
// ============================================
// Управляет циклической работой GPS и SIM модулей
// Реализует логику: сбор координат (GPS) → отправка данных (SIM)
// Интегрируется с SystemModes для энергосбережения
// ============================================
class TrackerController {
  private:
    // ===== УКАЗАТЕЛИ НА МОДУЛИ =====
    NEO* _gps;          // Указатель на GPS модуль (для получения координат)
    SIM* _sim;          // Указатель на SIM модуль (для отправки данных)
    MPU* _mpu;          // Указатель на MPU датчик (для детекции движения в режиме сна)
    
    // ===== ФЛАГИ СОСТОЯНИЯ =====
    bool _gpsDataReady;         // GPS данные получены и готовы к отправке?
                                // true - координаты есть, можно переключаться на SIM
                                
    bool _simCommandsDone;      // Все AT команды SIM выполнены?
                                // true - отправка завершена, цикл окончен
                                
    unsigned long _lastCommandTime;  // Время последней отправки AT команды (ms)
                                     // Используется для неблокирующей задержки 3 секунды
                                     
    int _commandIndex;          // Текущий индекс в массиве AT команд (0-9)
                                // Последовательно увеличивается до 10
    
    // ===== РЕЖИМ СНА =====
    bool _mpuMovementDetected;  // Обнаружено движение MPU датчиком?
                                // Используется для пробуждения из SLEEP_MODE
    
    // ===== ПРИВАТНЫЕ МЕТОДЫ =====
    
    // Отправляет следующую AT команду из списка
    // Вызывается каждые 3 секунды пока не выполнены все 10 команд
    void sendNextSimCommand();
    
    // Сбрасывает последовательность SIM команд
    // Устанавливает _commandIndex = 0, _simCommandsDone = false
    void resetSimCommandSequence();
    
  public:
    // ===== КОНСТРУКТОР =====
    // @param gps - указатель на объект GPS модуля
    // @param sim - указатель на объект SIM модуля  
    // @param mpu - указатель на объект MPU датчика
    TrackerController(NEO* gps, SIM* sim, MPU* mpu);
    
    // ===== ИНИЦИАЛИЗАЦИЯ =====
    // Вызывается один раз в setup()
    // Сбрасывает последовательность SIM команд
    void begin();
    
    // ===== ОСНОВНОЙ ЦИКЛ =====
    // Вызывается каждый раз в loop()
    // Выполняет:
    //   1. Обновление GPS и проверку получения данных
    //   2. Обновление SIM (если активен по режиму) и отправку команд
    //   3. Обновление состояния системы через SystemModes
    //   4. Проверку MPU в режиме сна
    void update();
    
    // ===== СБРОС СОСТОЯНИЯ =====
    // Полный сброс внутреннего состояния контроллера
    // Сбрасывает флаги и индексы для нового цикла
    void resetCycle();
    
    // ===== ПРОВЕРКА СОСТОЯНИЯ =====
    
    // Возвращает true, если GPS получил валидные данные
    bool isGPSDataReady();
    
    // Возвращает true, если все AT команды SIM выполнены
    bool isSIMCommandsDone();
    
    // Возвращает true, если MPU обнаружил движение
    // Используется для пробуждения из режима сна
    bool isMPUMovementDetected();
    
    // ===== ПРИНУДИТЕЛЬНОЕ УПРАВЛЕНИЕ =====
    
    // Принудительно запускает чтение GPS
    // Сбрасывает флаг _gpsDataReady и инициирует новое чтение
    void forceGPSRead();
    
    // Принудительно запускает последовательность SIM команд
    // Сбрасывает индекс команд и флаг выполнения
    void forceSIMCommands();
};

#endif