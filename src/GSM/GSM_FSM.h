#ifndef GSM_FSM_H
#define GSM_FSM_H

#include <Arduino.h>
#include "GSM.h"

// =====================================================
// GSM STATES
// =====================================================

enum class GSMState
{
    DISABLED,

    IDLE,

    WAIT_URL,

    MOCK_SEND,

    REAL_SEND,

    WAIT_RESPONSE,

    DONE
};

// =====================================================
// GSM FSM
// =====================================================

class GSM_FSM
{
private:

    // =====================================
    // GSM MODULE
    // =====================================

    GSM* _gsm;

    // =====================================
    // FSM
    // =====================================

    GSMState _state;

    // =====================================
    // FLAGS
    // =====================================

    bool _enabled;

    bool _mock;

    bool _done;

    // =====================================
    // DATA
    // =====================================

    char _url[128];

    unsigned long _t0;

    // =====================================
    // INTERNAL
    // =====================================

    void sendMock();

    void sendReal();

public:

    // =====================================
    // CONSTRUCTOR
    // =====================================

    GSM_FSM(GSM* gsm);

    // =====================================
    // SYSTEM
    // =====================================

    void begin();

    void update();

    // =====================================
    // MODULE CONTROL
    // =====================================

    void enable();

    void disable();

    bool isEnabled();

    // =====================================
    // FSM
    // =====================================

    void setState(GSMState state);

    GSMState getState();

    // =====================================
    // DATA
    // =====================================

    void setURL(const char* url);

    void setMode(bool mock);

    bool isDone();

    void reset();
};

#endif