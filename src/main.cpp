#include <Arduino.h>
#include "NEO/NEO.h"
#include "SIM/SIM.h"
#include "MPU/MPU.h"
#include "TestManager/TestManager.h"
#include "ModeManager/SystemModes.h"
#include "ModeManager/TrackerController.h"

// ============================================
// ВЫБОР РЕЖИМА РАБОТЫ (РАСКОММЕНТИРОВАТЬ НУЖНЫЙ)
// ============================================
// ВАЖНО: Только ОДИН из следующих макросов должен быть раскомментирован!
// ============================================

//#define MODE_DEBUG        // Режим отладки (активен по умолчанию)
                          // - Все модули работают одновременно
                          // - TestManager циклически отправляет AT команды
                          // - Полезно для проверки работы каждого модуля

#define MODE_TRACKER      // Режим трекера (GPS->SIM циклично)
                          // - GPS и SIM работают по очереди (энергосбережение)
                          // - Сбор координат → отправка данных → повтор
                          // - MPU отключен

//#define MODE_SLEEP        // Режим сна (пробуждение от MPU)
                          // - Устройство спит большую часть времени
                          // - MPU работает как детектор движения
                          // - При движении: пробуждение → сбор GPS → отправка → сон

// ============================================
// СОЗДАНИЕ ОБЪЕКТОВ МОДУЛЕЙ
// ============================================

// GPS модуль: RX на пине 3, TX на пине 4
// (Подключение: GPS TX → Arduino RX (пин 3))
NEO gps(3, 4);

// SIM800L модуль: RX на пине 5, TX на пине 6
// (Подключение: SIM800L TX → Arduino RX (пин 5))
SIM sim800l(5, 6);

// MPU6050 датчик движения/акселерометр
// (Подключение по I2C: SDA→A4, SCL→A5 на Arduino Uno/Nano)
MPU mpu6050;

// Контроллер трекера - управляет логикой сбора и отправки данных
TrackerController tracker(&gps, &sim800l, &mpu6050);

// Указатель на менеджер тестирования (используется только в DEBUG режиме)
TestManager* testManager = nullptr;

// ============================================
// ФУНКЦИЯ НАСТРОЙКИ (выполняется один раз при старте)
// ============================================
void setup() {
  // Инициализация последовательного порта для отладки
  // Скорость должна совпадать со скоростью в мониторе порта
  Serial.begin(9600);
  
  // Инициализация системы управления режимами
  // (загружает настройки таймингов, флаги состояний)
  SystemModes::begin();
  
  // ============================================
  // НАСТРОЙКА В ЗАВИСИМОСТИ ОТ ВЫБРАННОГО РЕЖИМА
  // ============================================
  
  #ifdef MODE_DEBUG
    // ---------- РЕЖИМ ОТЛАДКИ ----------
    // Устанавливаем режим DEBUG в системе
    SystemModes::setMode(OperationMode::DEBUG_MODE);
    
    // Создаём менеджер тестирования (динамически в куче)
    testManager = new TestManager(&gps, &sim800l, &mpu6050);
    
    // Запускаем тестирование (печатает шапку, инициализирует модули)
    testManager->begin();
    
  #elif defined(MODE_TRACKER)
    // ---------- РЕЖИМ ТРЕКЕРА ----------
    // Устанавливаем режим TRACKER
    SystemModes::setMode(OperationMode::TRACKER_MODE);
    
    // В режиме трекера MPU не нужен - отключаем его для экономии энергии
    mpu6050.disable();
    
    // Инициализируем контроллер трекера
    tracker.begin();
    
    // Настройка начального состояния: начинаем с GPS
    if (SystemModes::shouldGPSBeActive()) {
      gps.begin(9600);      // Включаем GPS
      sim800l.disable();    // SIM пока не нужен
    } 
    // Альтернативный старт (если по какой-то причине нужно начать с SIM)
    else if (SystemModes::shouldSIMBeActive()) {
      sim800l.begin(9600);  // Включаем SIM
      gps.disable();        // GPS отключаем
    }
    
  #elif defined(MODE_SLEEP)
    // ---------- РЕЖИМ СНА ----------
    // Устанавливаем режим SLEEP
    SystemModes::setMode(OperationMode::SLEEP_MODE);
    
    // Инициализируем контроллер трекера
    tracker.begin();
    
    // В режиме сна все основные модули отключаются для максимальной экономии
    gps.disable();          // GPS выключен в режиме сна
    sim800l.disable();      // SIM выключен в режиме сна
    
    // MPU работает как датчик пробуждения (детектор движения)
    mpu6050.begin();
  #endif
  
  // Сигнал о готовности системы (видно в мониторе порта)
  Serial.println("System ready!");
}

// ============================================
// ГЛАВНЫЙ ЦИКЛ (выполняется бесконечно)
// ============================================
void loop() {
  // ============================================
  // РЕЖИМ 1: ОТЛАДКА (DEBUG)
  // ============================================
  #ifdef MODE_DEBUG
    // Режим отладки - все модули работают одновременно
    // TestManager сам управляет циклической отправкой AT команд и выводом данных
    
    // Проверяем, создан ли менеджер тестирования
    if (testManager != nullptr) {
      // Обновляем состояние тестирования:
      // - Отправляет следующую AT команду каждые 3 секунды
      // - Читает и выводит ответы SIM модуля
      // - Выводит NMEA строки GPS
      // - Выводит данные MPU6050
      testManager->update();
    }
    
    // Небольшая задержка для стабильности работы
    // Предотвращает слишком частый опрос модулей
    delay(10);
  
  // ============================================
  // РЕЖИМ 2: ТРЕКЕР (TRACKER)
  // ============================================
  #elif defined(MODE_TRACKER)
    // Режим трекера - GPS и SIM работают по очереди (энергосбережение)
    // Активен только один модуль в каждый момент времени
    
    // ----- ФАЗА 1: СБОР КООРДИНАТ (GPS) -----
    // Проверяем, должен ли GPS быть активен по таймингу
    if (SystemModes::shouldGPSBeActive()) {
      // Если GPS выключен - включаем его и отключаем SIM
      if (!gps.isEnabled()) {
        gps.enable();                     // Включаем питание GPS
        sim800l.disable();                // Отключаем SIM для экономии энергии
        Serial.println("[Tracker] GPS active, SIM sleeping");
      }
      // Обновляем данные GPS (читаем координаты)
      tracker.update();
    }
    
    // ----- ФАЗА 2: ОТПРАВКА ДАННЫХ (SIM) -----
    // Проверяем, должен ли SIM быть активен по таймингу
    if (SystemModes::shouldSIMBeActive()) {
      // Если SIM выключен - включаем его и отключаем GPS
      if (!sim800l.isEnabled()) {
        sim800l.enable();                 // Включаем SIM модуль
        gps.disable();                    // Отключаем GPS для экономии энергии
        Serial.println("[Tracker] SIM active, GPS sleeping");
      }
      // Отправляем собранные координаты через SIM (GPRS/SMS)
      tracker.update();
    }
    
    // ----- ПРОВЕРКА ЗАВЕРШЕНИЯ ЦИКЛА -----
    // Определяем, завершён ли полный цикл (GPS + SIM)
    if (SystemModes::isTrackerCycleComplete()) {
      Serial.println("[Tracker] Cycle complete - resetting for next round");
      // Сбрасываем таймеры и флаги для начала нового цикла
      SystemModes::resetTrackerCycle();
      
      // Пауза между циклами для стабилизации сети и экономии энергии
      // Даёт время модулям "остыть" перед следующим циклом
      delay(1000);
    }
    
    // Задержка между итерациями цикла
    // Предотвращает чрезмерную загрузку процессора
    delay(100);
  
  // ============================================
  // РЕЖИМ 3: СОН С ПРОБУЖДЕНИЕМ ПО ДВИЖЕНИЮ (SLEEP)
  // ============================================
  #elif defined(MODE_SLEEP)
    // Режим глубокого сна с пробуждением от MPU6050
    // Устройство спит большую часть времени, экономя батарею
    
    // Проверяем текущий режим (дополнительная безопасность)
    if (SystemModes::getCurrentMode() == OperationMode::SLEEP_MODE) {
      
      // ----- ПРОВЕРКА: НУЖНО ЛИ УЙТИ В СОН? -----
      // Если цикл сбора/отправки данных завершён
      if (SystemModes::isTrackerCycleComplete()) {
        // Сбрасываем флаги цикла перед погружением в сон
        SystemModes::resetTrackerCycle();
        
        // УХОД В ГЛУБОКИЙ СОН
        // Код останавливается здесь до прерывания от MPU
        // MPU6050 отслеживает движение и генерирует прерывание
        SystemModes::goToSleep();
        // После пробуждения выполнение продолжится со следующей строки
      }
      
      // ----- ПРОСНУЛИСЬ! ВЫПОЛНЯЕМ ОДИН ЦИКЛ ТРЕКЕРА -----
      // Устройство проснулось от движения - нужно собрать и отправить данные
      
      // Фаза GPS: собираем текущие координаты
      if (SystemModes::shouldGPSBeActive()) {
        if (!gps.isEnabled()) {
          gps.enable();               // Включаем GPS
          sim800l.disable();          // SIM пока не нужен
        }
        tracker.update();             // Читаем координаты с GPS
      }
      
      // Фаза SIM: отправляем данные на сервер
      if (SystemModes::shouldSIMBeActive()) {
        if (!sim800l.isEnabled()) {
          sim800l.enable();           // Включаем SIM модуль
          gps.disable();              // Выключаем GPS (уже получили координаты)
        }
        tracker.update();             // Отправляем данные через SIM
      }
      
      // После выполнения цикла на следующей итерации loop()
      // условие isTrackerCycleComplete() станет true
      // и устройство снова уйдёт в глубокий сон
    }
    
    // Задержка для стабилизации работы модулей
    delay(100);
    
  #endif
  // ============================================
  // КОНЕЦ ОБРАБОТКИ РЕЖИМОВ
  // ============================================
}

// ============================================
// ПРИМЕЧАНИЯ ПО РАБОТЕ С РЕЖИМАМИ:
// ============================================
// 1. Всегда раскомментирован ТОЛЬКО ОДИН макрос (MODE_DEBUG, MODE_TRACKER или MODE_SLEEP)
// 2. При переключении режима нужно перекомпилировать и залить код заново
// 3. В режиме SLEEP MPU должен быть подключён к пину прерывания
// 4. Скорость Serial должна совпадать со скоростью в мониторе порта
// 5. Для реального энергосбережения нужны транзисторные ключи для GPS и SIM
